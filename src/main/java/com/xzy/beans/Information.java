public class Information {
    private ProductBrand productBrand;

    private ProductMaterial productMaterial;

    private ProductVersion productVersion;

    private ProductStyle productStyle;

    private ProductThickness productThickness;

    public ProductBrand getProductBrand() {
        return productBrand;
    }

    public void setProductBrand(ProductBrand productBrand) {
        this.productBrand = productBrand;
    }

    public ProductMaterial getProductMaterial() {
        return productMaterial;
    }

    public void setProductMaterial(ProductMaterial productMaterial) {
        this.productMaterial = productMaterial;
    }

    public ProductVersion getProductVersion() {
        return productVersion;
    }

    public void setProductVersion(ProductVersion productVersion) {
        this.productVersion = productVersion;
    }

    public ProductStyle getProductStyle() {
        return productStyle;
    }

    public void setProductStyle(ProductStyle productStyle) {
        this.productStyle = productStyle;
    }

    public ProductThickness getProductThickness() {
        return productThickness;
    }

    public void setProductThickness(ProductThickness productThickness) {
        this.productThickness = productThickness;
    }
}
